        <div class="content">
            <h2>Stack Technique</h2>
            <ul>
                <li>Sveltekit sans SSR ni Prerendering</li>
                <li>TypeScript</li>
                <li>Bulma</li>
            </ul>

            <h2>Description du Projet</h2>
            <p>
                Vous devez créer un générateur de quotes permettant aux utilisateurs de créer et visualiser des quotes pour des
                produits ou services.
            </p>

            <h3>Exigences Principales</h3>
            <ul>
                <li>Créer les types requis dans $lib/types.ts</li>
                <li>Créer les composants requis dans $lib/components</li>
                <li>Créer les pages requises dans $lib/routes</li>
                <li>Documenter les hypothèses et décisions</li>
                <li>Coder en anglais</li>
            </ul>

            <h3>Création de quotes</h3>
            <ul>
                <li>Les utilisateurs doivent pouvoir ajouter plusieurs lignes au quotes</li>
                <li>
                    Chaque ligne doit inclure :
                    <ul>
                        <li>Nom du produit/service</li>
                        <li>Prix unitaire</li>
                        <li>Quantité</li>
                    </ul>
                </li>
                <li>Les utilisateurs doivent pouvoir supprimer des lignes</li>
                <li>Les utilisateurs doivent pouvoir modifier des lignes existantes</li>
                <li>OPTIONNEL : Section informations société/client</li>
            </ul>

            <h3>Génération de quotes</h3>
            <ul>
                <li>Un bouton pour générer le quotes final</li>
                <li>
                    Le quotes généré doit afficher :
                    <ul>
                        <li>Sous-total</li>
                        <li>Montant de la TVA (20%)</li>
                        <li>Montant total</li>
                        <li>Date de création</li>
                        <li>Numéro/référence unique de quotes</li>
                    </ul>
                </li>
            </ul>

            <h3>Exigences UI/UX</h3>
            <ul>
                <li>Utiliser Bulma pour le style (CSS personnalisé minimal ou inexistant)</li>
                <li>Interface claire et intuitive</li>
            </ul>

            <h3>Fonctionnalités Bonus (Optionnelles)</h3>
            <ul>
                <li>Possibilité de sauvegarder les quotes (localStorage suffisant)</li>
                <li>Génération PDF/Impression</li>
            </ul>

            <h2>Suggestions</h2>
            <h4>Conseils pour les Candidats</h4>
            <ul>
                <li>Se concentrer d'abord sur les exigences principales</li>
                <li>Planifier la structure des types et des composants avant de coder, tu peux valider avec moi si tu veux</li>
                <li>Garder l'interface simple en utilisant les composants intégrés de Bulma</li>
            </ul>

